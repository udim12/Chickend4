package EMA.chickend.Logic.Interfaces;

/**
 * The IGame interface represents the game.
 */
public interface IGame {
    /**
     * void PlayGame()
     *     Purpose: start the game.
     */
    public void playGame();
    /**
     * void ExitGame()
     *     Purpose: exit the game and return to the main menu.
     */
    public void exitGame();
}
